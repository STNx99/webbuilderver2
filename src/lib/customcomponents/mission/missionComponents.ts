// Helper function to create customizable badges
const createBadge = (
  text: string,
  bgColor: string,
  textColor: string,
  style: "rounded" | "pill" | "square" = "rounded"
) => {
  const borderRadius = {
    rounded: "8px",
    pill: "30px",
    square: "0px",
  };

  return {
    type: "Text" as const,
    content: text,
    id: uuidv4(),
    isSelected: false,
    isHovered: false,
    isDraggedOver: false,
    styles: {
      display: "inline-block",
      backgroundColor: bgColor,
      padding: "4px 12px",
      borderRadius: borderRadius[style],
      fontSize: "14px",
      color: textColor,
      fontWeight: "500",
      marginBottom: "8px",
    },
    tailwindStyles: `inline-block ${
      style === "pill"
        ? "rounded-full"
        : style === "rounded"
        ? "rounded-lg"
        : ""
    } px-3 py-1 text-sm font-medium mb-2`,
    href: "",
    src: "",
    parentId: "",
    projectId: "",
  };
};

// Function to create flexible mission content layout
const createMissionLayout = (layout: "left" | "right" | "center") => {
  const baseComponent = {
    type: "Frame" as const,
    name: `MissionComponent_${layout}`,
    id: uuidv4(),
    content: "",
    isSelected: false,
    isHovered: false,
    isDraggedOver: false,
    styles: {
      width: "100%",
      padding: "40px 20px",
    },
    tailwindStyles: "w-full py-10 px-5",
    elements: [],
    href: "",
    src: "",
    parentId: "",
    projectId: "",
  };

  // Different layout configurations
  if (layout === "left" || layout === "right") {
    baseComponent.styles = {
      ...baseComponent.styles,
    };
    baseComponent.tailwindStyles =
      "grid grid-cols-1 md:grid-cols-2 gap-16 items-center py-10 px-5";
  } else if (layout === "center") {
    baseComponent.styles = {
      ...baseComponent.styles,
    };
    baseComponent.tailwindStyles =
      "flex flex-col items-center text-center max-w-4xl mx-auto py-10 px-5";
  }

  return baseComponent;
};

